{ /* Non-technical questions

  Tell me about yourself? (1 min to pitch, stand out)
    - tell the heroe's journey, your life success story, stand out, why you are better
    - mention things you want to get asked
    - skills should be relevant to job

  Why do you want to work with us? (stand out)
    - show why they are top choise, make them feel special
      Example: I have reseached your company for a while, I have done a work similar to this in a past
      I think I can add value and learn smth new so I can become better with you

  Why did you leave your last job?
    - no negativity, be as positive as you can
    - I want to grow, new challenges, new success

  Tell me about a problem you solved (interesing project you worked on)?
    - prepare a problem that you solved (situation --> action --> result)
    - have metrics and numbers
    - scaling, performance, security (how I improved it)
    - technologies should relate

  What is your biggest weakness?
    - real answer
    - show how you improved it
      Example: start coding before thinking on solution, so lately I do sudocode with comments before real coding

  What is my biggest strength?
    - I am a nice, positive person with
    - no ego, team player, never complain but work towards win/win solution
    - I am experienced developer
    - My priority is to make client happy, I understand both business & development
    - I can talk in easy maner about hard tech stuff, so managers will understand my point
    - I work not only for money, I am intersested to resolve a problem and provide good solution

  What to ask the interviewer? (stand out)
    - focus on interviewer (YOU)
    - mention smth interviewer mentioned
    - ask about the role / tech stack, my ponential team, about company, any conflicts, remote word.
      Example: Why did you join this company, how do you feel here, where company is going to be in next 5 years?
      What they wish they were told when they joined the company?

*/}

